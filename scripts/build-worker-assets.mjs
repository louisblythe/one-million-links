import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const root = resolve(import.meta.dirname, "..");
const workerPath = resolve(root, "worker/index.js");
const generatedPath = resolve(root, "worker/generated-assets.js");

const [workerSource, css, clientJs, ogImage, appleTouchIcon] = await Promise.all([
  readFile(workerPath, "utf8"),
  readFile(resolve(root, "public/assets/app.css"), "utf8"),
  readFile(resolve(root, "public/assets/app.js"), "utf8"),
  readFile(resolve(root, "public/og-image.png")),
  readFile(resolve(root, "public/apple-touch-icon.png")),
]);

function replaceEmbeddedAsset(source, functionName, value) {
  const pattern = new RegExp(`function ${functionName}\\(\\) \\{\\n  return "(?:[^"\\\\]|\\\\.)*";\\n\\}`, "s");
  const replacement = `function ${functionName}() {\n  return ${JSON.stringify(value)};\n}`;
  if (!pattern.test(source)) {
    throw new Error(`Could not find embedded ${functionName}() asset.`);
  }
  return source.replace(pattern, replacement);
}

let nextWorker = replaceEmbeddedAsset(workerSource, "css", css);
nextWorker = replaceEmbeddedAsset(nextWorker, "clientJs", clientJs);

const generated = `// Generated by scripts/build-worker-assets.mjs. Do not edit directly.\nexport const OG_IMAGE_PNG_BASE64 = ${JSON.stringify(ogImage.toString("base64"))};\nexport const APPLE_TOUCH_ICON_PNG_BASE64 = ${JSON.stringify(appleTouchIcon.toString("base64"))};\n`;

await Promise.all([
  writeFile(workerPath, nextWorker),
  writeFile(generatedPath, generated),
]);
